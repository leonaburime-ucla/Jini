function parseStorageKey(input: RouteInputContext): Result<string> {
  const key = input.params.key;
  return typeof key === 'string' && key.length > 0 ? ok(key) : err(validationError('key must be a non-empty path parameter'));
}

interface StoragePutRequest {
  readonly key: string;
  readonly dataBase64: string;
  readonly contentType?: string;
}

function parseStoragePut(input: RouteInputContext): Result<StoragePutRequest> {
  const keyResult = parseStorageKey(input);
  if (!keyResult.ok) return keyResult;
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  const dataBase64 = input.body.dataBase64;
  if (typeof dataBase64 !== 'string' || dataBase64.length === 0) {
    return err(validationError('dataBase64 must be a non-empty base64-encoded string', [{ path: 'dataBase64', message: 'required base64 string' }]));
  }
  const contentType = input.body.contentType;
  if (contentType !== undefined && typeof contentType !== 'string') {
    return err(validationError('contentType must be a string when provided'));
  }
  return ok({ key: keyResult.value, dataBase64, ...(contentType !== undefined ? { contentType: contentType as string } : {}) });
}

function parseStorageListQuery(input: RouteInputContext): Result<string | undefined> {
  const prefix = firstQueryValue(input.query.prefix);
  return ok(prefix);
}

