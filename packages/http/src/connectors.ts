function parseAuthCredentials(input: RouteInputContext): Result<AuthCredentials> {
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  const email = nonEmptyString(input.body.email);
  if (!email) return err(validationError('email must be a non-empty string', [{ path: 'email', message: 'required non-empty string' }]));
  const password = nonEmptyString(input.body.password);
  if (!password) return err(validationError('password must be a non-empty string', [{ path: 'password', message: 'required non-empty string' }]));
  return ok({ email, password });
}

function parseAuthToken(input: RouteInputContext): Result<string> {
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  const token = nonEmptyString(input.body.token);
  if (!token) return err(validationError('token must be a non-empty string', [{ path: 'token', message: 'required non-empty string' }]));
  return ok(token);
}

function parseAuthSessionQuery(input: RouteInputContext): Result<string> {
  const token = firstQueryValue(input.query.token);
  if (!token || token.trim().length === 0) {
    return err(validationError('token query parameter is required', [{ path: 'token', message: 'required non-empty string' }]));
  }
  return ok(token);
}

