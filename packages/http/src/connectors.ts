function parseDbCollection(input: RouteInputContext): Result<string> {
  const collection = input.params.collection;
  return typeof collection === 'string' && collection.length > 0
    ? ok(collection)
    : err(validationError('collection must be a non-empty path parameter'));
}

interface DbInsertRequest {
  readonly collection: string;
  readonly record: DbRecord;
}

function parseDbInsert(input: RouteInputContext): Result<DbInsertRequest> {
  const collectionResult = parseDbCollection(input);
  if (!collectionResult.ok) return collectionResult;
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  const id = input.body.id;
  if (typeof id !== 'string' || id.length === 0) {
    return err(validationError('id must be a non-empty string field', [{ path: 'id', message: 'required non-empty string' }]));
  }
  return ok({ collection: collectionResult.value, record: input.body as unknown as DbRecord });
}

interface DbCollectionIdParams {
  readonly collection: string;
  readonly id: string;
}

function parseDbCollectionId(input: RouteInputContext): Result<DbCollectionIdParams> {
  const collectionResult = parseDbCollection(input);
  if (!collectionResult.ok) return collectionResult;
  const id = input.params.id;
  if (typeof id !== 'string' || id.length === 0) return err(validationError('id must be a non-empty path parameter'));
  return ok({ collection: collectionResult.value, id });
}

interface DbUpdateRequest extends DbCollectionIdParams {
  readonly patch: Readonly<Record<string, unknown>>;
}

function parseDbUpdate(input: RouteInputContext): Result<DbUpdateRequest> {
  const parsed = parseDbCollectionId(input);
  if (!parsed.ok) return parsed;
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  return ok({ ...parsed.value, patch: input.body });
}

interface DbQueryRequest {
  readonly collection: string;
  readonly query?: DbQuery;
}

function parseDbQueryRequest(input: RouteInputContext): Result<DbQueryRequest> {
  const collectionResult = parseDbCollection(input);
  if (!collectionResult.ok) return collectionResult;
  const whereRaw = firstQueryValue(input.query.where);
  if (whereRaw === undefined) return ok({ collection: collectionResult.value });
  let parsedWhere: unknown;
  try {
    parsedWhere = JSON.parse(whereRaw);
  } catch {
    return err(validationError('where must be valid JSON when provided'));
  }
  if (!isRecord(parsedWhere)) return err(validationError('where must be a JSON object when provided'));
  return ok({ collection: collectionResult.value, query: { where: parsedWhere } });
}

