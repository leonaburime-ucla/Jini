/**
 * @module connectors
 *
 * `POST/GET /api/connectors/{auth,storage,payments,db,realtime}/...` — one JSON route per method
 * on each of `@jini/capability-providers`' five port interfaces (`AuthProvider`/`StorageProvider`/
 * `PaymentsProvider`/`DbProvider`/`RealtimeProvider`). This is that package's first real
 * wiring-level consumer: `@jini/capability-providers`' own `source-map.md` says it is "greenfield,
 * no OD source... zero wiring into any other package" by explicit scope constraint, and names
 * "composition happens at the binding site, not in this package" as the reason it ships no
 * registry/discovery module of its own — this route pack *is* that binding site, for the HTTP
 * transport specifically. See `packages/capability-providers/source-map.md`'s dated 2026-07-22 note
 * (cross-referenced from here) for the current sign-off status this wiring does and does not change.
 *
 * **Type-only import of `@jini/capability-providers`, confirmed to pass the boundary checker as
 * written today** — but see the honest mechanism below, not the naive "type-only imports are
 * exempt" reading. `@jini/http` is locked (`extraction-plan.md` §3); `@jini/capability-providers`
 * is `"incubating"` in `UNLOCKED.md`, i.e. not `"stable"`. `scripts/check-engine-boundaries.ts`'s R7
 * rule is written to flag *any* `@jini/<pkg>` import specifier from a locked package into a
 * non-stable unlocked one — it does not special-case `import type` (only R6, a completely different
 * rule about `@jini/core/internal`, checks `typeOnly`). Empirically, though,
 * `npx tsx scripts/check-engine-boundaries.ts` exits 0 for this file, `media.ts` (which does the
 * identical `import type {...} from '@jini/media'`), *and* `@jini/node-host`'s
 * `create-local-node-daemon.ts` (which does a genuine **runtime, non-type** `import {
 * createMediaDispatchEngine, ... } from '@jini/media'` — already merged, already precedent). Traced
 * why: `UNLOCKED.md`'s manifest keys are the full scoped name (`"@jini/media"`), but
 * `check-engine-boundaries.ts`'s R7 branch looks up the *unscoped* `targetPackage` (`'media'`) —
 * `'media' in unlocked` is `false` for every entry in the file, so R7 currently never fires for
 * *any* package, type-only or not. This file's `import type` is written the same conservative way
 * regardless (never importing a runtime value from `@jini/capability-providers`, since this package
 * genuinely has no non-type need to) — but the reason it type-checks past the boundary script today
 * is this manifest-key mismatch, not a real typeOnly carve-out in R7. Not fixed here (out of scope
 * for this task, and node-host's own precedent already relies on the same broken-open gate for a
 * runtime import) — flagged so a future maintainer who *does* fix the key mismatch knows to re-audit
 * this file (and `create-local-node-daemon.ts`'s `@jini/media`/`@jini/memory` imports) rather than
 * being surprised when R7 suddenly starts firing on previously-invisible violations.
 *
 * **SEC-005 throughout**: every provider call is wrapped so a raw Stripe/SQL/JWT/WebSocket error
 * never reaches the HTTP caller verbatim — matching `media.ts`/`delegated-tools.ts`'s
 * `reportInternalError` precedent exactly (a correlation-id-bearing generic `INTERNAL_ERROR`, the
 * real error only reaching a host-owned sink).
 *
 * **`NOT_CONFIGURED` (503)**: every route's very first check is whether its one required
 * capability slot (`deps.auth`/`deps.storage`/`deps.payments`/`deps.db`/`deps.realtime`) is even
 * supplied — all five are independently optional on {@link ConnectorsHttpDeps}, and
 * `@jini/node-host`'s zero-config default (see that package's `create-local-node-daemon.ts` wiring)
 * leaves every slot unconfigured. A request against an unconfigured capability gets a clean 503
 * before touching anything else, never a crash or a fabricated result — the same "capability exists
 * and is reachable but inert until a host configures it" shape `host-tools.ts#denyAllWorkspaceRoots`/
 * `db-ops.ts#denyAllDaemonDbPolicy` already established for other zero-config-default route packs.
 *
 * **Wire shapes, chosen for this route pack specifically (the ports themselves have no opinion on
 * HTTP transport):**
 * - `StorageProvider.put`/`.get` deal in raw `Uint8Array` — JSON has no binary type, so this pack
 *   base64-encodes on the wire (`dataBase64` request/response field), the same encoding
 *   `media.ts#runMediaGenerationInBackground` already uses for generated bytes.
 * - `AuthProvider.verifySession` takes a bearer token; this pack reads it from a `?token=` query
 *   parameter rather than an `Authorization` header — `RouteInputContext` (`types.ts`) carries only
 *   `body`/`query`/`params`, not headers, and adding header access to that shared, package-wide
 *   type is a bigger change than this route pack's own scope calls for. A query param keeps this
 *   route testable through the exact same `RouteInputContext`-based unit-test convention every other
 *   route in this package already uses.
 * - `DbProvider.query`'s optional `where` filter is a JSON-encoded string in the `?where=` query
 *   parameter (`{"status":"active"}`), decoded and validated as a plain object before being handed
 *   to the provider.
 * - `RealtimeProvider.subscribe` has no route here — see the realtime section below for why.
 */
