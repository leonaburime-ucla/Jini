export interface ConnectorsInternalErrorContext {
  readonly source: 'auth' | 'storage' | 'payments' | 'db' | 'realtime';
  readonly operation: string;
  readonly correlationId: string;
  readonly error: unknown;
}

export interface ConnectorsHttpDeps {
  readonly auth?: AuthProvider;
  readonly storage?: StorageProvider;
  readonly payments?: PaymentsProvider;
  readonly db?: DbProvider;
  readonly realtime?: RealtimeProvider;
  /** Host-owned sink for the real exception behind a generic `INTERNAL_ERROR` response (SEC-005). Defaults to `console.error`. */
  readonly onInternalError?: (context: ConnectorsInternalErrorContext) => void;
}

function defaultInternalErrorSink(context: ConnectorsInternalErrorContext): void {
  // eslint-disable-next-line no-console
  console.error(`[@jini/http] internal error (connectors/${context.source}.${context.operation}, correlationId=${context.correlationId})`, context.error);
}

function notConfiguredError(capability: string): ReturnType<typeof createApiError> {
  return createApiError('NOT_CONFIGURED', `${capability} provider not configured`);
}

/** Runs one provider call, converting any thrown error into a redacted, correlation-id-bearing `INTERNAL_ERROR` (SEC-005) instead of letting it reach the HTTP caller verbatim. */
async function guardedCall<T>(
  deps: ConnectorsHttpDeps,
  source: ConnectorsInternalErrorContext['source'],
  operation: string,
  fn: () => Promise<T>,
): Promise<Result<T>> {
  try {
    return ok(await fn());
  } catch (error) {
    const correlationId = randomUUID();
    const sink = deps.onInternalError ?? defaultInternalErrorSink;
    sink({ source, operation, correlationId, error });
    return err(createApiError('INTERNAL_ERROR', 'an internal error occurred', { requestId: correlationId }));
  }
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

function nonEmptyString(value: unknown): string | null {
  return typeof value === 'string' && value.trim().length > 0 ? value : null;
}

function firstQueryValue(raw: unknown): string | undefined {
  const value = Array.isArray(raw) ? raw[0] : raw;
  return typeof value === 'string' ? value : undefined;
}

