function parseChargeInput(input: RouteInputContext): Result<ChargeInput> {
  if (!isRecord(input.body)) return err(validationError('body must be a JSON object'));
  const amountCents = input.body.amountCents;
  if (typeof amountCents !== 'number' || !Number.isFinite(amountCents)) {
    return err(validationError('amountCents must be a finite number', [{ path: 'amountCents', message: 'required number' }]));
  }
  const currency = nonEmptyString(input.body.currency);
  if (!currency) return err(validationError('currency must be a non-empty string', [{ path: 'currency', message: 'required non-empty string' }]));
  const customerRef = nonEmptyString(input.body.customerRef);
  if (!customerRef) {
    return err(validationError('customerRef must be a non-empty string', [{ path: 'customerRef', message: 'required non-empty string' }]));
  }
  const description = input.body.description;
  if (description !== undefined && typeof description !== 'string') {
    return err(validationError('description must be a string when provided'));
  }
  return ok({ amountCents, currency, customerRef, ...(description !== undefined ? { description: description as string } : {}) });
}

function parseChargeId(input: RouteInputContext): Result<string> {
  const id = input.params.id;
  return typeof id === 'string' && id.length > 0 ? ok(id) : err(validationError('id must be a non-empty path parameter'));
}

